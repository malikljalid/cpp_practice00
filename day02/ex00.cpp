#include <iostream>

int main(void)
{
    int A, B;

    std::cout << "Please give a number A : ";
    std::cin >> A;
    std::cout << "Please give a number B : ";
    std::cin >> B;
    std::cout << "A + B = " << A + B << '\n';
    std::cout << "A - B = " << A - B << '\n';
    std::cout << "A * B = " << A * B << '\n';
    std::cout << "A / B = " << A / B << '\n';
    std::cout << "A % B = " << A % B << '\n';
    
    return (0);
}
