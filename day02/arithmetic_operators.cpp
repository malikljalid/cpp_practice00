#include <iostream>

int main(void)
{
    int A, B;

    A = 20;
    B = 10;
    
    std::cout << "A + B = " << A + B << '\n';
    std::cout << "A - B = " << A - B << '\n';
    std::cout << "A * B = " << A * B << '\n';
    std::cout << "A / B = " << A / B << '\n';
    std::cout << "A % B = " << A % B << '\n';
    
    return (0);
}
